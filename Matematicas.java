/** clase que va a contener métodos estáticos de cálculo matemático
 * @author Antonio
 * @version 1.0
 */
 
public class Matematicas {
	/** método que devuelve si un número es par o no
	 * @param numero entero
	 * @return boolean
	 */ 
	public static boolean esPar (int numero) {
		return numero % 2 == 0;
	}
}
